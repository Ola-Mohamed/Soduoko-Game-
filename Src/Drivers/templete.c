// Function to choose a random matrix
int chooseRandomMatrix( int numMatrices) {
    // Seed the random number generator
    srand(time(NULL));

    // Generate a random index within the range of the number of matrices
    int randomIndex = rand() % numMatrices;

    // Return the randomly chosen matrix
    return randomIndex;
}


